const INDICE = 13;
let soma = 0;
let k = 0;

while (k < INDICE) {
  k++; // k = k + 1
  soma += k; // soma = soma + k
}

console.log(soma); // 91

/*

Lógica do código:

Enquanto o k for menor do que INDICE (13), a variável k é incrementada e a variável soma vai recebendo o valor de k a cada iteração. No final, o valor de soma é impresso no console (91).

*/
